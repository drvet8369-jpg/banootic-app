-- Banootik App Schema
-- This script is designed to be idempotent. It can be run multiple times without causing errors.
-- It first drops all existing objects in the correct order and then recreates them from scratch.

-- --------------------------------------------
-- STEP 1: DROP EXISTING OBJECTS
-- --------------------------------------------
-- Drop the trigger on auth.users first as it depends on a function.
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;

-- Drop the function that the trigger calls.
DROP FUNCTION IF EXISTS public.handle_new_user();

-- Drop tables. Using CASCADE will automatically drop dependent objects 
-- like policies, foreign key constraints, and sequences. This is the most robust way.
DROP TABLE IF EXISTS public.reviews;
DROP TABLE IF EXISTS public.profiles;
DROP TABLE IF EXISTS public.services;
DROP TABLE IF EXISTS public.categories;
DROP TABLE IF EXISTS public.one_time_passwords;


-- --------------------------------------------
-- STEP 2: CREATE TABLES
-- --------------------------------------------

-- Table for Categories
CREATE TABLE public.categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    slug TEXT NOT NULL UNIQUE,
    description TEXT
);
COMMENT ON TABLE public.categories IS 'Stores the main service categories for Banootik (e.g., Beauty, Cooking).';

-- Table for Services
CREATE TABLE public.services (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    slug TEXT NOT NULL UNIQUE,
    category_id BIGINT NOT NULL REFERENCES public.categories(id)
);
COMMENT ON TABLE public.services IS 'Stores the sub-services within each category (e.g., Manicure, Haircut).';

-- Table for Profiles (connected to auth.users)
CREATE TABLE public.profiles (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    full_name TEXT,
    account_type TEXT NOT NULL DEFAULT 'customer',
    phone TEXT UNIQUE,
    bio TEXT,
    location TEXT,
    profile_image_url TEXT,
    service_id BIGINT REFERENCES public.services(id),
    rating NUMERIC(2, 1) DEFAULT 0.0,
    reviews_count INT DEFAULT 0
);
COMMENT ON TABLE public.profiles IS 'Stores public profile information for all users (customers and providers), linked to Supabase auth.';

-- Table for Reviews
CREATE TABLE public.reviews (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    author_id UUID NOT NULL REFERENCES public.profiles(id) ON DELETE CASCADE,
    rating INT NOT NULL CHECK (rating >= 1 AND rating <= 5),
    comment TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE public.reviews IS 'Stores reviews and ratings given by customers to providers.';

-- Table for One-Time Passwords (for phone authentication)
CREATE TABLE public.one_time_passwords (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    phone TEXT NOT NULL UNIQUE,
    token TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
COMMENT ON TABLE public.one_time_passwords IS 'Temporarily stores OTPs for phone verification.';


-- --------------------------------------------
-- STEP 3: CREATE FUNCTIONS AND TRIGGERS
-- --------------------------------------------

-- Function to create a new profile when a new user signs up in auth.users
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO public.profiles (id, full_name, phone, account_type)
    VALUES (
        new.id,
        new.raw_user_meta_data->>'full_name',
        new.phone,
        new.raw_user_meta_data->>'account_type'
    );
    RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger to call the function after a new user is created
CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- --------------------------------------------
-- STEP 4: SET UP ROW-LEVEL SECURITY (RLS)
-- --------------------------------------------
-- Enable RLS for all tables
ALTER TABLE public.categories ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.services ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.reviews ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.one_time_passwords ENABLE ROW LEVEL SECURITY; -- Important for security

-- Policies for 'categories'
CREATE POLICY "Allow public read access to categories" ON public.categories FOR SELECT USING (true);

-- Policies for 'services'
CREATE POLICY "Allow public read access to services" ON public.services FOR SELECT USING (true);

-- Policies for 'profiles'
CREATE POLICY "Allow public read access to profiles" ON public.profiles FOR SELECT USING (true);
CREATE POLICY "Allow users to update their own profile" ON public.profiles FOR UPDATE USING (auth.uid() = id);

-- Policies for 'reviews'
CREATE POLICY "Allow public read access to reviews" ON public.reviews FOR SELECT USING (true);
CREATE POLICY "Allow authenticated users to insert reviews" ON public.reviews FOR INSERT WITH CHECK (auth.role() = 'authenticated');
CREATE POLICY "Allow users to delete their own reviews" ON public.reviews FOR DELETE USING (auth.uid() = author_id);

-- Policies for 'one_time_passwords'
-- No policies needed for OTP table as it should only be accessed by the admin/service_role key.
-- By default, with RLS enabled and no policies, no one can access it.

-- --------------------------------------------
-- END OF SCHEMA
